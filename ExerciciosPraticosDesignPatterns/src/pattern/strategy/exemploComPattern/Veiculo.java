package pattern.strategy.exemploComPattern;

public abstract class Veiculo {

	private String nome;
	private String marca;

	
	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getMarca() {
		return marca;
	}

	public void setMarca(String marca) {
		this.marca = marca;
	}

}
