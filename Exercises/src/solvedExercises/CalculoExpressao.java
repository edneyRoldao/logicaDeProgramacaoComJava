package solvedExercises;
 /**
  * 
  * @author edneyroldao
  *Escreva um algoritmo que leia tres numeros inteiros e positivos (A, B, C) e calcule a seguinte expressao:
  * D = (R + S / 2), onde R = ((A + B)^2) e S = ((B + C)^2) 
  */
public class CalculoExpressao {

	public static void main(String[] args) {
		System.out.println(calcularExpressao(10, 20, 30));
	}
	
	public static int calcularExpressao(int A, int B, int C) {
		int R = (A + B) * (A + B);
		int S = (B + C) * (B + C);
		
		return (R + S) / 2;
	}
	
	
}
